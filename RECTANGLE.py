#wap to find area and perimeter of rectangle.
base_of_rec=float(input("Enter base of rectangle"))
width_of_rec=float(input("enter width of rectangle"))
area=base_of_rec*width_of_rec
perimeter=(2*base_of_rec)+(2*width_of_rec)
print("Area of rectangle is ",area)
print("perimeter of rectangle is ",perimeter)